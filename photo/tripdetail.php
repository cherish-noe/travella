  <h1 style="margin-left:200px; color:#ff8000"> ITINERARY </h1>
  <h3 style="margin-left:100px; color:#ff8000"> DAY 1: START YANGON; CITY TOUR INCLUDING SHWEDAGON PAGODA. </h3>
  <p style="margin-left:100px;text-align:justify;margin-right:100px;"> We have some free time to recover from the flight before enjoying a short tour of this teeming city which ends at the
magnificent Shwedagon Pagoda. The pagoda is 2,500 years old and the most religious site in Myanmar that is said to
contain eight hairs of the Buddha as well as 5500 diamonds and other precious stones. There will be a welcome
briefing this evening with an optional group dinner at a local restaurant. </p>
<h3 style="margin-left:100px; color:#ff8000"> DAY 2: Mandalay CITY</h3>
<p style="margin-left:100px;text-align:justify;margin-right:100px;"> This morning we leave Yangon and drive to Kyaikhtiyo Pagoda (Golden Rock). Along the way we visit the former
capital, Bago City and see some of the Buddha statues and temples that it is famous for. In the afternoon we begin ourapproach to the Kyaikhtiyo Pagoda which involves boarding an open truck at the foot of Mount Kyaikhtiyo for an 11km
drive along dirt roads followed by a 45 minute walk up to the important pilgrimage site. Completely covered in gold leaf
and delicately balanced on the edge of a cliff high above the coastal plains, this renowned monument is an experience
to rival the Shwedagon Pagoda or the breath-taking beauty of Bagan, particularly when the sun begins to set.</p>

<h3 style="margin-left:100px; color:#ff8000"> DAY 3: Mandalay  CITY </h3>
<p style="margin-left:100px;text-align:justify;margin-right:100px;"> After breakfast we descend along the dirt tracks back to our vehicle and then proceed to Hpa An, the capital city of
Karen (Kayin) state. On arrival, we visit Kawgun Cave where we can view thousands of tiny Buddha carvings dating
back to the 7th century all over the walls of this open cavern. We then move on to Kyauk Ka Latt, a pagoda mounted on
a striking pinnacle of rock balancing vertically on its own in the middle of a lake. We then end the day enjoying sunset
from the Shwe Yin Myaw Pagoda, located on the bank of Than Lwin River.  </p>

<h3 style="margin-left:100px; color:#ff8000"> DAY 4: Sagaing  CITY </h3>
<p style="margin-left:100px;text-align:justify;margin-right:100px;"> Today we depart for Sagaing, the capital of Sagaing . On the way we stop to visit Saddan Cave, one of the
highlights of the area. As you enter the enormous limestone cavern you will be greeted by statues of the Buddha, a
couple of pagodas and (if you venture far enough) truck-sized stalactites and walls of crystal. We board canoes and
then emerge from the far side of the cave onto an idyllic hidden lake. Following this we proceed to Mawlamyaine
through Karen villages and limestone hills. Upon arrival we then have some free time to explore the third largest city in
Myanmar which was also the first British settlement in the country. </p> 

<h3 style="margin-left:100px; color:#ff8000"> DAY 5: Bagan  CITY </h3>
<p style="margin-left:100px;text-align:justify;margin-right:100px;"> Today you'll be picked up at your hotel at 9AM to begin your showstopper tour of Bagan. Along the way you'll make stops at:

Nyaung Oo Market, Shwezigon Pagoda, Gubyaukgyi Temple, Ananda Temple, Dhammayangyi, a lacquerware factory and end with sunset at Shwesandaw Pagoda. Break for lunch in Old Bagan in the midst of all this, for a much needed rest. </p>



